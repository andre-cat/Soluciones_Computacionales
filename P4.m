%{
Un tanque esférico tiene un orificio circular en el fondo a través del
cual fluye líquido. La tasa de flujo a través del agujero se calcula como:
    
    Q = CA√(2gH)

Donde Q = flujo de salida (m3/s), C = coeficiente obtenido en forma
empírica, A = área del orificio (m2), g = la aceleración gravitacional
(9.81 m/s2) y H = profundidad del líquido dentro del tanque.

Ya que el orificio tiene un diámetro de 3 cm y C = 0.55, ¿cuánto tiempo
tomaría para que el agua fluyera por completo de un tanque de 3 m de
diámetro con altura inicial de 2.75 m? Utilice los siguientes métodos
numéricos:
a) Euler.
b) Euler Mejorado.
c) Runge-Kutta 4to orden.
%}

